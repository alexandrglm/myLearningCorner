public class RelationalOperators {

    public static void main(String[] args) {

        int a = 10;
        int b = 20;

        // Equal to (==)
        System.out.println(" a == b : " + (a == b ));

        // Not Equal !=
        System.out.println(" a != b : " + (a != b));

        // Greater than >
        System.out.println(" a > b : " + (a > b));

        // Less than <  
        System.out.println(" a < b : " + (a < b));

        // Greater or Equal >=
        System.out.println(" a >= b : " + (a >= b));

        // Less or Equal
        System.out.println(" a <= b :" + (a <= b));

    }

}