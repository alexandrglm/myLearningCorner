package com.example;

public class DataTypesExamples {

    public static void main(String[] args) {

        /*
         DATA TYPES USED

         1      NUMBERS
         1.1    Integers
         1.2    Decimals

         2      BOOLEANS

         3      STRINGS
         */

        // 1.1  Integers
        byte    variableOne     = 100;          // Max 10² long
        short   variableTwo     = 10000;        // Max 10³ long
        int     variableThree   = 1000000000;   // Max. 10⁹ long
        long    variableFour    = 1000000000;   // Max. 10⁹ long

        // 1.2  Decimals
        float   variableFive    = 5.5f;
        double  variableSix     = 10.5d;
        variableSix = 20.5d;    // As let as coding JS, but in Java: a variable has been redeclared like this
        double  productPrice = 40.99d;  // An example

        // 2    BOOLEANS
        boolean variableSeven   = true;
        boolean variableEight   = false;

        // 3    STRING
        char    variableNine    = 'a';
        String  variableTen     = "Lorem ipsum dolor sit anet ...";
        
    }
    
}
